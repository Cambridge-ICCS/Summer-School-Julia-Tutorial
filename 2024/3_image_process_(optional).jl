		return t
	end
	
	ts = [0.1, 0.2, 0.3]
	
	@time @assert sum(task, ts) == sum(ts)
	println()
	
	@time let tasks = [@Threads.spawn task(t) for t in ts]
		@assert sum(fetch, tasks) == sum(ts)
